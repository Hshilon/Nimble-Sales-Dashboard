  const [search, setSearch] = useState("");
  
  const funders = ["הכל", ...new Set(ACTIVE.map(r=>r["מממן"]).filter(Boolean))];
  const filtered = ACTIVE.filter(r=>
    (filter==="הכל"||r["מממן"]===filter) &&
    (!search||r["לקוח"].includes(search)||r["ספק"].includes(search)||r["מספר בקשה"].includes(search))
  );
