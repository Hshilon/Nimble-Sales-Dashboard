function Pipeline() {
  const [filter, setFilter] = useState("הכל");
  
  const managers = ["הכל", ...new Set(PIPELINE.map(d=>d.manager).filter(Boolean))];
  const filtered = filter==="הכל"?PIPELINE:PIPELINE.filter(d=>d.manager===filter);
  const totalAll = PIPELINE.reduce((s,d)=>s+(d.value||0),0);
  const totalHigh = PIPELINE.filter(d=>d.prob==="גבוהה").reduce((s,d)=>s+(d.value||0),0);
  const byMgr = {};
  PIPELINE.forEach(d=>{if(!d.manager)return;if(!byMgr[d.manager])byMgr[d.manager]=0;byMgr[d.manager]+=d.value||0;});
  const totS = filtered.reduce((s,d)=>s+(d.value||0),0);

  return (
    <div>
      <div className="cards">
        <StatCard label="סה״כ פייפליין" value={fmtILS(totalAll)} sub={PIPELINE.length+" עסקאות"} />
        <StatCard label="צפי גבוהה" value={fmtILS(totalHigh)} sub={PIPELINE.filter(d=>d.prob==="גבוהה").length+" עסקאות"} />
        {Object.entries(byMgr).map(([m,v]) => <StatCard key={m} label={"מנהל: "+m} value={fmtILS(v)} sub="" />)}
      </div>
      <div className="filters">
        {managers.map(m => (
          <button key={m} className={`filter-btn${m===filter?" active":""}`} onClick={()=>setFilter(m)}>{m}</button>
        ))}
      </div>
      <div className="tbl-wrap">
        <table>
          <thead><tr><th>מנהל תיק</th><th>לקוח</th><th>ספק</th><th>סוג מימון</th><th>צפי סכום</th><th>סבירות</th></tr></thead>
          <tbody>
            {filtered.map((d,i) => {
              const pc = d.prob==="גבוהה"?"wait":d.prob==="נמוכה"?"mer":"dis";
              const tc = d.type==="ריברס"?"nim":"dis";
              return (
                <tr key={i}>
                  <td>{d.manager}</td>
                  <td>{d.client}</td>
                  <td>{d.vendor}</td>
                  <td><span className={`badge b-${tc}`}>{d.type}</span></td>
                  <td>{fmtILS(d.value)}</td>
                  <td><span className={`badge b-${pc}`}>{d.prob}</span></td>
                </tr>
              );
            })}
          </tbody>
          <tfoot><tr><td colSpan="4">סה״כ ({filtered.length} עסקאות)</td><td>{fmtILS(totS)}</td><td></td></tr></tfoot>
        </table>
      </div>
    </div>
  );
}

